public class RoleRule {
    private String roleRuleName;
    private Boolean roleRuleValue;

    public String getRoleRuleName() {
        return roleRuleName;
    }

    public void setRoleRuleName(String roleRuleName) {
        this.roleRuleName = roleRuleName;
    }

    public Boolean getRoleRuleValue() {
        return roleRuleValue;
    }

    public void setRoleRuleValue(Boolean roleRuleValue) {
        this.roleRuleValue = roleRuleValue;
    }
}
